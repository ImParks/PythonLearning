# 정수 자료형

a = 123
print(a)
print(type(a))
a = 0
print(a)
print(type(a))
a = -213
print(a)
print(type(a))

# 실수 자료형
a = 1.2
print(a)
print(type(a))

a = -3.45
print(a)
print(type(a))

a = 0.0
print(a)
print(type(a))

# 컴퓨터식 지수 표현방식
a = 4.24e10
print(a)
print(type(a))

a = 4.24e-10
print(a)
print(type(a))


# 8진수와 16진수
a = 0o177
print(a)
print(type(a))
